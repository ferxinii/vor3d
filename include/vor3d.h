#ifndef VOR3D_MAIN_H
#define VOR3D_MAIN_H

#include "vdiagram.h"
#include "trimesh.h"

/* ---- Convex domain ---- */

// Copy of seeds inside s_vdiagram
// out_kept_idx, if non-NULL, must point to an array of seeds->N ints; on
// return, out_kept_idx[i] is the index of input seed i inside the returned
// s_vdiagram's seeds/vcells, or -1 if that seed was dropped (e.g. it was not
// strictly interior to the boundary).
//
// vor3d_in_bp takes a pre-built s_bpoly -- use this when calling
// repeatedly against the same boundary (e.g. growing a tree one bifurcation
// at a time), to build the bpoly once and amortize that cost across calls.
// vor3d_in_convh is the one-shot convenience wrapper: it builds the
// bpoly from convh on every call and frees it before returning.
s_vdiagram vor3d_in_bp(const s_points *seeds, const s_bpoly *bp, double vol_max_rel_diff,
                       double EPS_DEG, double TOL, int (*randint)(void*, int),
                       void *rctx, s_dynarray *buff_points, int *out_kept_idx);

// Same as vor3d_in_bp, but also transfers ownership of the seed DT to *out_dt
// (caller must free_complex() it) instead of freeing it internally. Real seeds
// occupy DT vertex ids 0..vd.seeds.N-1, matching vd.vcells[].seed_id -- so
// vertex_neighbors() on this DT gives the Voronoi cell adjacency (Delaunay dual).
// Internal use by Stage C (ncvx.c); not needed by ordinary callers.
s_vdiagram vor3d_in_bp_dt(const s_points *seeds, const s_bpoly *bp, double vol_max_rel_diff,
                          double EPS_DEG, double TOL, int (*randint)(void*, int),
                          void *rctx, s_dynarray *buff_points, int *out_kept_idx,
                          s_scplx *out_dt);

s_vdiagram vor3d_in_convh(const s_points *seeds, const s_convh *convh, double vol_max_rel_diff,
                           double EPS_DEG, double TOL, int (*randint)(void*, int),
                           void *rctx, s_dynarray *buff_points, int *out_kept_idx);

// Mirror-free convex Voronoi build (IMPROVE_CONVEX.md Idea C). Builds a lean
// seeds-only DT (no mirror points) and clips each cell against the fan
// tetrahedralization of bp with exact LP-feasibility predicates. `seeds` must be
// the already interior-filtered seeds; out_kept_idx (nullable, length
// n_kept_idx) is composed in place with the DT compaction remap, as in the
// mirror path. Used internally by vor3d_core; exposed for the A/B benchmark.
s_vdiagram vor3d_convex_clip(const s_points *seeds, const s_bpoly *bp,
                             double EPS_DEG, double TOL,
                             int *out_kept_idx, int n_kept_idx);

// Seeds generated by Poisson disc sampling
s_vdiagram vor3d_in_bp_pds(double (*f_radius_poiss)(double*, void*), void *f_params,
                            const s_bpoly *bp, double vol_max_rel_diff,
                            double EPS_DEG, double TOL,
                            int (*randint)(void*, int), double (*randd01)(void*), void *rctx,
                            s_dynarray *buff_points);
                            // Deep copy of bp inside


/* ---- Non-convex domain ---- */

// vor3d_in_ncvx_domain takes a pre-built s_ncvx_domain -- use this when calling
// repeatedly against the same domain to amortize the CDT construction cost.
// vor3d_in_trimesh is the one-shot convenience wrapper.
//
// The convex-hull pieces (vcell.pieces / N_pieces) and per-cell volume are
// always produced. If want_surface is true, the cell boundary trimesh(es)
// (vcell.surface / N_surface, one per connected component) are also built;
// otherwise they are left NULL/0.
//
// If merge_orphans is true, a post-pass reassigns every disconnected cell
// component ("orphan", left across a concavity) to a neighbouring seed so that
// each seed owns exactly one spatially-connected region, exported as a single
// closed trimesh with volume conserved (see MERGING_ORPHANS.md). This pass is
// driven by the surface accumulators, so it REQUIRES the surface machinery:
// passing merge_orphans=true forces want_surface on internally (the merged
// surface is produced regardless of the want_surface argument).
s_ncvx_vdiagram vor3d_in_ncvx_domain(const s_points *seeds, const s_ncvx_domain *domain,
                                 double vol_max_rel_diff,
                                 double EPS_DEG, double TOL,
                                 int (*randint)(void*, int), void *rctx,
                                 s_dynarray *buff_points, int *out_kept_idx,
                                 bool want_surface, bool merge_orphans);

s_ncvx_vdiagram vor3d_in_trimesh(const s_points *seeds, const s_trimesh *mesh,
                                  double vol_max_rel_diff,
                                  double EPS_DEG, double TOL,
                                  int (*randint)(void*, int), void *rctx,
                                  s_dynarray *buff_points, int *out_kept_idx,
                                  bool want_surface, bool merge_orphans);

#endif
